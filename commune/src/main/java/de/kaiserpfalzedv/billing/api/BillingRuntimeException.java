package de.kaiserpfalzedv.billing.api;

import java.util.Objects;
import java.util.UUID;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

/**
 * @author klenkes {@literal <rlichti@kaiserpfalz-edv.de>}
 * @version 1.0.0
 * @since 2018-02-14
 */
public class BillingRuntimeException extends RuntimeException {
    private final UUID id = UUID.randomUUID();

    public BillingRuntimeException(String message) {
        super(message);
    }

    public BillingRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }

    public BillingRuntimeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    @Override
    public int hashCode() {
        return Objects.hash(getId());
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (!(o instanceof BillingBusinessException)) return false;
        BillingBusinessException that = (BillingBusinessException) o;
        return Objects.equals(getId(), that.getId());
    }

    public UUID getId() {
        return id;
    }

    @Override
    public String toString() {
        return new ToStringBuilder(this, ToStringStyle.SHORT_PREFIX_STYLE)
                .appendSuper(super.toString())
                .append("id", id)
                .toString();
    }
}
